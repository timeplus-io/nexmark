  auction  BIGINT,
  bidder  BIGINT,
  price  BIGINT,
  date_time  TIMESTAMP(3),
  extra  VARCHAR,
  d1 VARCHAR,
  d2 VARCHAR
) WITH (
  'connector' = 'kafka',
  'topic' = 'NEXMARK_Q10',
