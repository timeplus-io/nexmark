CREATE TABLE nexmark_q0 (
  auction  BIGINT,
  bidder  BIGINT,
  price  BIGINT,
  date_time  TIMESTAMP(3),
  extra  VARCHAR
) WITH (
  'connector' = 'kafka',
  'topic' = 'NEXMARK_Q0',
  'properties.bootstrap.servers' = 'kafka:9092',
